// Contact Form
function handleFormSubmit(e) {
  e.preventDefault();
  const dict = i18n[currentLanguage] || i18n.en;
  const btn = e.target.querySelector('button[type="submit"]');
  btn.disabled = true;
  btn.textContent = dict.misc.sending;
  setTimeout(() => {
    document.getElementById("form-success").style.display = "block";
    e.target.reset();
    btn.disabled = false;
    btn.innerHTML = `${dict.misc.sent} <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" style="width:1rem;height:1rem;display:inline-block;vertical-align:middle;margin-left:.4rem"><line x1="5" y1="12" x2="19" y2="12"/><polyline points="12 5 19 12 12 19"/></svg>`;
    applyLanguage(currentLanguage);
  }, 1400);
}
window.handleFormSubmit = handleFormSubmit;


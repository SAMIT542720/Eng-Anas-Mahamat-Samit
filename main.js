/** Maps any known translation string (EN/FR/AR) -> canonical English key used in i18n.*.text */
function buildTextReverseMap() {
  const map = new Map();
  ["en", "fr", "ar"].forEach((lang) => {
    const block = i18n[lang].text;
    Object.keys(i18n.en.text).forEach((enKey) => {
      const val = block[enKey];
      if (val != null && String(val).trim()) map.set(String(val).trim(), enKey);
    });
  });
  return map;
}
const textReverseMap = buildTextReverseMap();

function translateSelectOptions(selectEl, dict) {
  if (!selectEl) return;
  Array.from(selectEl.options).forEach((opt) => {
    const t = opt.textContent.trim();
    const enKey = textReverseMap.get(t) || (Object.prototype.hasOwnProperty.call(i18n.en.text, t) ? t : null);
    if (enKey && dict.text[enKey] != null) opt.textContent = dict.text[enKey];
  });
}

function translateMobileLanguageOptions(dict) {
  const sel = document.getElementById("language-select-mobile");
  if (!sel) return;
  const mapValToKey = { en: "English", fr: "Francais", ar: "Arabic" };
  Array.from(sel.options).forEach((opt) => {
    const enKey = mapValToKey[opt.value];
    if (enKey && dict.text[enKey] != null) opt.textContent = dict.text[enKey];
  });
}


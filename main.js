function applyLanguage(lang) {
  currentLanguage = i18n[lang] ? lang : "en";
  localStorage.setItem("siteLanguage", currentLanguage);
  const dict = i18n[currentLanguage];

  document.documentElement.lang = currentLanguage;
  document.documentElement.dir = dict.dir;
  document.body.classList.toggle("rtl", dict.dir === "rtl");

  if (dict.pageTitle && !document.body.hasAttribute("data-static-page-title")) document.title = dict.pageTitle;

  document.querySelectorAll("#language-select, #language-select-mobile").forEach((s) => {
    s.value = currentLanguage;
  });

  Object.entries(dict.html).forEach(([selector, html]) => {
    const node = document.querySelector(selector);
    if (node) node.innerHTML = html;
  });

  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      if (!node.nodeValue || !node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
      const parent = node.parentElement;
      if (!parent) return NodeFilter.FILTER_REJECT;
      if (["SCRIPT", "STYLE", "SVG", "PATH", "TEXT"].includes(parent.tagName)) return NodeFilter.FILTER_REJECT;
      if (parent.closest("#page-loader")) return NodeFilter.FILTER_REJECT;
      if (parent.closest("#language-select") || parent.closest("#language-select-mobile")) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    }
  });
  const textNodes = [];
  while (walker.nextNode()) textNodes.push(walker.currentNode);
  textNodes.forEach((node) => {
    const raw = node.nodeValue;
    const trimmed = raw.trim();
    if (!trimmed) return;
    const enKey =
      textReverseMap.get(trimmed) || (Object.prototype.hasOwnProperty.call(i18n.en.text, trimmed) ? trimmed : null);
    if (!enKey) return;
    const next = dict.text[enKey];
    if (next == null) return;
    node.nodeValue = raw.replace(trimmed, next);
  });

  Object.entries(dict.placeholders).forEach(([selector, value]) => {
    const el = document.querySelector(selector);
    if (el) el.setAttribute("placeholder", value);
  });

  const fs = document.getElementById("form-success");
  if (fs && dict.misc.formSuccess) fs.textContent = dict.misc.formSuccess;

  translateSelectOptions(document.getElementById("service"), dict);
  translateMobileLanguageOptions(dict);

  document.querySelectorAll('label[for="language-select"], label[for="language-select-mobile"]').forEach((lb) => {
    const t = lb.textContent.trim();
    const k = textReverseMap.get(t) || (Object.prototype.hasOwnProperty.call(i18n.en.text, t) ? t : null);
    if (k && dict.text[k] != null) lb.textContent = dict.text[k];
  });

  const langSel = document.getElementById("language-select");
  if (langSel) langSel.setAttribute("aria-label", dict.aria.languageSelector);
  const langMob = document.getElementById("language-select-mobile");
  if (langMob) langMob.setAttribute("aria-label", dict.aria.mobileLanguageSelector);

  const backTop = document.getElementById("back-top");
  if (backTop) backTop.setAttribute("aria-label", dict.aria.backToTop);

  const footerEmail = document.getElementById("footer-email");
  if (footerEmail) footerEmail.setAttribute("aria-label", dict.aria.newsletterEmail);

  document.querySelectorAll(".portfolio-video").forEach((v) => {
    if (dict.aria.siteWalkthroughVideo) v.setAttribute("aria-label", dict.aria.siteWalkthroughVideo);
  });

  document.querySelectorAll(".team-overlay a[title], .footer-social[title]").forEach((a) => {
    const title = a.getAttribute("title");
    if (!title) return;
    const k = textReverseMap.get(title.trim()) || (Object.prototype.hasOwnProperty.call(i18n.en.text, title.trim()) ? title.trim() : null);
    if (k && dict.text[k] != null) a.setAttribute("title", dict.text[k]);
  });

  const aboutSvg = document.querySelector("#about .about-image svg[role='img']");
  if (aboutSvg) {
    const aboutAria =
      dict.text["Illustration of a modern Chadian courtyard home"] ||
      dict.text["Illustration of a modern African courtyard home"];
    if (aboutAria) aboutSvg.setAttribute("aria-label", aboutAria);
  }

  refreshCrossPageLanguageLinks();
}

document.querySelectorAll("#language-select, #language-select-mobile").forEach((selectEl) => {
  selectEl.addEventListener("change", (e) => applyLanguage(e.target.value));
});

